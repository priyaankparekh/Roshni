        String[] name=null;
        for(String[] color:colorList){
            r=Integer.parseInt(color[1]);
            g=Integer.parseInt(color[2]);
            b=Integer.parseInt(color[3]);
            min=((red-r)*(red-r)+(green-g)*(green-g)+(blue-b)*(blue-b))/3;
            if(max>min){
                max=min;
                name=color;
            }
        }
